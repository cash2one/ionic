package com.thinkgem.jeesite.modules.api.to;


public class ProjectDateNodeResp {

	private String onLineDt;		// 上线时间
	private String startFundingDt;		// 开始募资时间
	private String endFundingDt;		// 募资结束时间
	private String finishRepayDt;		// 还款结束时间
	public String getOnLineDt() {
		return onLineDt;
	}
	public void setOnLineDt(String onLineDt) {
		this.onLineDt = onLineDt;
	}
	public String getStartFundingDt() {
		return startFundingDt;
	}
	public void setStartFundingDt(String startFundingDt) {
		this.startFundingDt = startFundingDt;
	}
	public String getEndFundingDt() {
		return endFundingDt;
	}
	public void setEndFundingDt(String endFundingDt) {
		this.endFundingDt = endFundingDt;
	}
	public String getFinishRepayDt() {
		return finishRepayDt;
	}
	public void setFinishRepayDt(String finishRepayDt) {
		this.finishRepayDt = finishRepayDt;
	}
	
	
	
}
